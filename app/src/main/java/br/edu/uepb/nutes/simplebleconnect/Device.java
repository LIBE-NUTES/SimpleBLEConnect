public class Device {

    private String name;
    private String manufactured;
    private String model;
    private String address;
    private String value;

    public Device(String name, String address) {
        this.name = name;
        this.address = address;
    }

    public Device(String name, String manufactured, String model, String address) {
        this.name = name;
        this.manufactured = manufactured;
        this.model = model;
        this.address = address;
    }

    public Device(String name, String manufactured, String model, String address, String value) {
        this.name = name;
        this.manufactured = manufactured;
        this.model = model;
        this.address = address;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getManufactured() {
        return manufactured;
    }

    public void setManufactured(String manufactured) {
        this.manufactured = manufactured;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    @Override
    public boolean equals(Object obj) {
        return this.getAddress().equals(((Device) obj).getAddress());
    }
}
